# ==========================================
# PYTHON OPERATORS
# ==========================================

a = 10
b = 5

# Arithmetic Operators
print("Addition =", a + b)
print("Subtraction =", a - b)
print("Multiplication =", a * b)
print("Division =", a / b)
print("Floor Division =", a // b)
print("Modulus =", a % b)
print("Exponent =", a ** b)

print("--------------------------------")

# Comparison Operators
print("a > b :", a > b)
print("a < b :", a < b)
print("a == b :", a == b)
print("a != b :", a != b)
print("a >= b :", a >= b)
print("a <= b :", a <= b)

print("--------------------------------")

# Logical Operators
x = True
y = False

print("x and y =", x and y)
print("x or y =", x or y)
print("not x =", not x)